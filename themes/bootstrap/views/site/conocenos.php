		<h3>BIENVENIDA</h3>
		<p>El Archivo Histórico de Monterrey, le da la más cordial bienvenida y para su comodidad pone a su disposición en este portal nuestro acervo documental que resguarda una parte fundamental de la memoria  histórica de nuestra ciudad. Estamos a sus órdenes.</p>
	</div>
</div>
<div class="row">
	<div class="col-lg-12 well">
		<h3>QUIÉNES SOMOS</h3>
		<p>El Archivo Histórico de Monterrey es el organismo adscrito a la Secretaría del Ayuntamiento que está encargado de tener la salva guarda y custodia de los libros y documentos con antigüedad mayor de cincuenta años, así como la clasificación y el ordenamiento que le dan la identidad a la Ciudad de Monterrey.</p>
	</div>
</div>
<div class="row">
	<div class="col-lg-12 well">
		<h3>MISIÓN</h3>
		<p>Es la custodia y el resguardo de los documentos históricos del Gobierno Municipal de la Ciudad de Monterrey.</p>
	</div>
</div>
<div class="row">
	<div class="col-lg-12 well">
		<h3>VISIÓN</h3>
		<p>Ser una dependencia que brinde servicios de calidad, con tecnología de punta y que compita con estándares de calidad.</p>
	</div>
</div>
<div class="row">
	<div class="col-lg-12 well">
		<h3>OBJETIVO</h3>
		<p>Preservar el acervo documental del municipio aplicando las nuevas tecnologías al Archivo Histórico en apoyo a la educación y a 
			la labor de investigación llevando a cabo la Administración del contenido del acervo en medios electrónicos.</p>
	</div>
</div>
<div class="row well">
	<div class="col-lg-12">
		<h3>PREGUNTAS FRECUENTES</h3>
		<div class="well">
			<p><strong>1) ¿Qué tipo de documentos puedo consultar en el acervo del archivo?</strong></p>
			<p>24 colecciones que comprenden temas diversos como ramo civil, actas de cabildo, protocolos, causas criminales, bandos, 
				entre otros.</p>
		</div>
		<div class="well">
			<p><strong>2) ¿Resguardan Actas de Nacimiento?</strong></p>
			<p>No. El acervo no cuenta con este tipo de documentos. Lo puede solicitar a través del Registro Civil del Gobierno del Estado 
				de Nuevo León.</p>
			<p><a target="_blank" href="http://archivo.nl.gob.mx/?P=reg_civiles">Registro Civil</a>
		</div>
		<div class="well">
			<p><strong>3) ¿Tiene algún costo la consulta?</strong></p>
			<p>No, el acceso a consultar y visualizar las imágenes es sin costo.</p>
		</div>
		<div class="well">
			<p><strong>4) Si requiero certificar documentos o copias simples de algún documento, ¿esto tiene algún costo?</strong></p>
			<p>El costo de la certificación de los documentos es de una cuota equivalente a un día de salario mínimo vigente en la zona. De 
				acuerdo al Art. 57 de la Ley de Hacienda para los Municipios del Estado de Nuevo León.</p>
		</div>
		<div class="well">
			<p><strong>5) En caso de no poder localizar el documento que busco, ¿me pueden brindar asesoría y/o apoyo en la búsqueda?</strong></p>
			<p>Por supuesto que sí, el personal de atención al público se pone a sus órdenes. puede ser vía telefónica al Tel. 5102-7280 de 
				lunes a viernes en el horario de 8:00 a 16:00 hrs. o bien puede acudir a nuestras instalaciones en el mismo horario y con 
				todo gusto le apoyaremos en la búsqueda y localización del documento o libro que desea consultar.</p>
		</div>
		<div class="well">
			<p><strong>6) ¿Brindan facilidades para visitas guiadas para estudiantes y escuelas?</strong></p>
			<p>En el Archivo Histórico nos interesa recibir grupos de estudiantes que deseen consultar el acervo del Archivo Histórico. Para informarse
				de los requisitos de consulta física, estamos a sus órdenes Tel. 5102-7280.</p>
		</div>
		<div class="well">
			<p><strong>7) ¿Se puede tener acceso a los documentos en físico o sólo consulta a través del portal?</strong></p>
			<p>La intención de concretar el proyecto de digitalización de todo el acervo del Archivo Histórico y ponerlo al alcance de todos 
				a través de este portal web, es de conservar en las mejores condiciones los documentos y libros en físico para la posteridad. 
				Se hizo un esfuerzo para que la digitalización fuera con la mejor resolución posible y así evitar el menor contacto posible 
				con los documentos más antiguos. 
				En caso de requerir una consulta física, favor de ponerse en contacto con el Encargado del Archivo Histórico para que le brinde 
				los requisitos.</p>
		</div>
	</div>
